package com.kk_electronic.kkportal.core.ui;

import com.google.gwt.uibinder.client.UiBinder;
import com.google.gwt.user.client.ui.Composite;
import com.google.gwt.user.client.ui.Panel;
import com.google.gwt.user.client.ui.Widget;
import com.google.inject.Inject;

public class Placeholder extends Composite {
	public static interface UIBinder extends UiBinder<Panel, Placeholder>{}
	
	@Inject
	public Placeholder(UIBinder binder) {
		initWidget(binder.createAndBindUi(this));
	}

	public void copysize(Widget widget) {
		this.setPixelSize(widget.getOffsetWidth(), widget.getOffsetHeight());
	}
}
